# Comparison Operators => That are used for comparison and their result is either true or false.

x = 4
y = 5

print(x == y)  # x equal y result => False
print(x != y)  # x not equal y result => True
print(x > y)  #  x greater than y result => False
print(x < y)  # x less than y result => True
print(x >= y)  # x greater than or equal to y result => False
print(x <= y)  # x greater than or equal to y result => True
