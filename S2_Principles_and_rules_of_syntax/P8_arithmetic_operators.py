x = 20
y = 7

print(x + y)  # additional operator result => 27
print(x - y)  # subtraction operator result => 13
print(x * y)  # multiplication operator result => 140
print(x / y)  # division operator result => 2.857142857142857
print(x % y)  # modulus operator result => 6
print(x**y)  # exponentiation operator result => 1280000000
print(x // y)  # floor division operator result => 2
