# Identifiers are names given to variables, functions, classes, etc
#
# Python is case sensitive.
#
# The names of the variables should start with "_, upper case and lower case" and the snake_case method should be used to separate the words.
#
# There is no problem with numbers and letters after "_, upper case and lower case"
#
# You can't use #, $, %, spaces, etc

_a = 10
a21a_ = 20
A = 30
_12 = 40

# Functions are named both by camelCase and snake_case methods
#
# Classes are named in the PascalCase.
